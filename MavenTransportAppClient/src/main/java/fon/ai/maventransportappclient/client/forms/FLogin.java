    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String username = jTextFieldUsername.getText();
        String password = String.copyValueOf(jPasswordFieldPassword.getPassword());
        if(username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Morate popuniti polja za logovanje!");
            return;
        }
        try {
            User user = new User();
            user.setUsername(username);
            user.setPassword(password);
            
            user = CommunicationController.getInstance().login(user);
            System.out.println(user);
            
            JOptionPane.showMessageDialog(this, "Uspe≈°no ste prijavljeni!");
            JFrame main = new FMainForm();
            main.setVisible(true);
            dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Neuspesno logovanje!");
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

